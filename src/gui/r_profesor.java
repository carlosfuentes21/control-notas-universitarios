    private void regActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regActionPerformed
        // TODO add your handling code here:
        if (!nom.getText().equals("") && !doc.getText().equals("")) {

            profesor e = lp.retunrcab();
            boolean esta = false;
            while (e != null) {
                if (e.getNom().equals(nom.getText())
                        || e.getDoc().equals(doc.getText())) {
                    esta = true;
                    JOptionPane.showMessageDialog(null, "NO SE PUEDE REGISTRAR EL DOCENTE");
                }
                e = e.sig;

            }
            if (esta == false) {
                String z = (String) prog.getSelectedItem();
                String s = (String) sex.getSelectedItem();
                String vp[] = z.split("-");
                programa p = lpro.buscar(vp[0]);

                profesor x = new profesor(doc.getText(), nom.getText(), ape.getText(), s, "activo", p);
                lp.ins_prof(x);
                a.GenerarArchivo("listadeprofesores.xls", lp.imprimir());
                lp.cargar(tabla);
                limpiar();
            }
        } else {
            JOptionPane.showMessageDialog(null, "HAY ESPACIOS SIN LLENAR");
        }
    }//GEN-LAST:event_regActionPerformed

    private void busActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busActionPerformed
        // TODO add your handling code here:
        if (doc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DIGITE EL CODIGO");
        } else {
            profesor a = lp.buscar(doc.getText());
            if (a != null) {
                if (a.getDoc().equals(doc.getText())) {
                    nom.setText(a.getNom());
                    ape.setText(a.getApe());
                    doc.setText(a.getDoc());
                    sex.setSelectedItem(a.getSexo());
                    prog.setSelectedItem(a.getP().getCod() + "-" + a.getP().getNom());
                    mod.setEnabled(true);
                    lim.setEnabled(true);
                    reg.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(null, "DOCENTE NO ENCONTRADO");
                }
            }
        }
    }//GEN-LAST:event_busActionPerformed

