    private void modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modActionPerformed
        // TODO add your handling code here:
        int n = JOptionPane.showConfirmDialog(null, "ESTA SEGURO DE MODIFICAR ESTE DOCENTE");
        if (n == 0) {
            String q = (String) prog.getSelectedItem();
            String s = (String) sex.getSelectedItem();
            String vp[] = q.split("-");
            programa p = lpro.buscar(vp[0]);
            lp.modificar(nom.getText(), ape.getText(), doc.getText(), s, p);
            mod.setEnabled(false);
            lim.setEnabled(false);
            reg.setEnabled(true);
            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "EL DOCENTE NO FUE MODIFICADO");
        }
        lp.cargar(tabla);
        a.GenerarArchivo("listadeprofesores.xls", lp.imprimir());
    }//GEN-LAST:event_modActionPerformed

