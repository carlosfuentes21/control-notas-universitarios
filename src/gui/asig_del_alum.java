    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        int filaseleccionada;
        try {
            filaseleccionada = tabla.getSelectedRow();
            if (filaseleccionada == -1) {
                JOptionPane.showMessageDialog(null, "NO SE HA SELECCIONADO NINGUNA MATERIA");
            } else {
                DefaultTableModel modelotabla = (DefaultTableModel) tabla.getModel();
                if (modelotabla.getValueAt(filaseleccionada, 0) != null) {
                    String codigo = (String) modelotabla.getValueAt(filaseleccionada, 0);
                    String nombre = modelotabla.getValueAt(filaseleccionada, 1).toString();
                    asignatura as = la.buscar(codigo);
                    cod.setText(codigo);
                    nom.setText(nombre);
                    nota.setText(lns.cargarnota(l, as));
                }
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex + "\nInt√©ntelo nuevamente",
                    " .::Error En la Operacion::.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tablaMouseClicked

