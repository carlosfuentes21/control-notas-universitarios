    private void notaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_notaKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "DIGITE SOLO NUMEROS");
        }
    }//GEN-LAST:event_notaKeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!cod.getText().equals("") && !nom.getText().equals("") && !nota.getText().equals("")) {
            String a = nota.getText();
            float not = Float.parseFloat(a);
            if (not < 5 && not > 0) {
                notas_semestre n = lns.buscaralum(l.getDoc());
                lns.asignarnota(l, cod.getText(), nota.getText());
                prom.setText(Float.toString(lns.promedio(l)));;
                n.setPromedio(prom.getText());
                JOptionPane.showMessageDialog(null, "LA NOTA FUE ASIGNADA");
            } else {
                JOptionPane.showMessageDialog(null, "LA NOTA NO ES VALIDA");
            }
        } else {
            JOptionPane.showMessageDialog(null, "HAY ESPACIOS SIN LLENAR");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

