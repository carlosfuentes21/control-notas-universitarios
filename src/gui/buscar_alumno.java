    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int filaseleccionada;
        try {
            filaseleccionada = tabla.getSelectedRow();
            if (filaseleccionada == -1) {
                JOptionPane.showMessageDialog(null, "NO SE HA SELECCIONADO NINGUNA MATERIA");
            } else {
                DefaultTableModel modelotabla = (DefaultTableModel) tabla.getModel();

                String codigo = (String) modelotabla.getValueAt(filaseleccionada, 2);
                alumno a = ll.buscar(codigo);
                asig_del_alum ada = new asig_del_alum(lns, a, la);
                ada.setVisible(true);

            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex + "\nInt√©ntelo nuevamente",
                    " .::Error En la Operacion::.", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void docKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_docKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "DIGITE SOLO NUMEROS");
        } else {
            doc.addKeyListener(new KeyAdapter() {
                public void keyReleased(final KeyEvent e) {
                    String cadena = (doc.getText()).trim();
                    doc.setText(cadena);
                    repaint();
                    filtro();
                }
            });
            tr = new TableRowSorter(tabla.getModel());
            tabla.setRowSorter(tr);
        }
    }//GEN-LAST:event_docKeyTyped

