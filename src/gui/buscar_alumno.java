    public void mostrarprogramas() {
        String a = (String) fac.getSelectedItem();
        String vf[] = a.split("-");
        prog.removeAllItems();
        programa aux = lp.retunrcab();
        while (aux != null) {
            if (aux.getFac().getCod().equals(vf[0])) {
                prog.addItem(aux.getCod() + "-" + aux.getNom());
            }
            aux = aux.sig;
        }
    }

