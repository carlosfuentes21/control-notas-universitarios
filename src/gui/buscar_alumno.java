    private void facItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_facItemStateChanged
        // TODO add your handling code here:
         if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (this.fac.getSelectedIndex() >= 0) {
                String z = fac.getSelectedItem().toString();
                String v[] = z.split("-");
                ll.alumnofacultad(tabla, prog, v[0], lf, lp);
                s = v[0];
            }
        }
    }//GEN-LAST:event_facItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (doc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DIGITE EL DOCUMENTO");
        } else {
            alumno a = ll.buscar(doc.getText());
            if (a != null) {
                if (a.getDoc().equals(doc.getText()) && !a.getest().equals("inactivo")) {
                    nom.setText(a.getNom());
                    ape.setText(a.getApe());
                    doc.setText(a.getDoc());
                    sex.setText(a.getSexo());
                    fac.setSelectedItem(a.getFac().getCod() + "-" + a.getFac().getNom());

                } else {
                    JOptionPane.showMessageDialog(null, "ALUMNO NO ENCONTRADO");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

